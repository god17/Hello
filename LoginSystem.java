    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        readEmployeeData();
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        // Check login credentials
        boolean loggedIn = false;
        for (String[] emp : employees) {
            if (emp[1].equals(username) && emp[3].equals(password)) {
                loggedIn = true;
                UserSession.setLoggedInUserId(emp[0]);
                if (emp[2].equals("1")) {
                    JOptionPane.showMessageDialog(LoginSystem.this, "Logged in as Manager");
                    new ManagerialEmployeeDirectory().setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(LoginSystem.this, "Logged in as Normal Employee");
                    new NormalEmployeeDirectory().setVisible(true);
                    this.setVisible(false);
                }
                break;
            }
        }
        if (!loggedIn) {
            JOptionPane.showMessageDialog(LoginSystem.this, "Invalid credentials");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        readEmployeeData();
        String secret = JOptionPane.showInputDialog(LoginSystem.this, "Enter Secret Key:");
        if (secret != null && secret.equals(secretKey)) {
            String newUsername = JOptionPane.showInputDialog(LoginSystem.this, "Enter New Username:");
            String newPassword = JOptionPane.showInputDialog(LoginSystem.this, "Enter New Password:");
            int newId = Integer.parseInt(employees.get(employees.size() - 1)[0].substring(1)) + 1;
            String newEmployeeId = "E" + String.format("%04d", newId);
            addNewUser(newEmployeeId, newUsername, newPassword, "0"); // Add as normal employee
            JOptionPane.showMessageDialog(LoginSystem.this, "Registration Successful");
        } else {
            JOptionPane.showMessageDialog(LoginSystem.this, "Invalid Secret Key");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

