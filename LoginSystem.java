    private void readEmployeeData() {
        String line;
        String cvsSplitBy = ",";
        try (BufferedReader br = new BufferedReader(new FileReader(csvFile))) {
            while ((line = br.readLine()) != null) {
                String[] emp = line.split(cvsSplitBy);
                employees.add(emp);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
   
    private void addNewUser(String employeeId, String username, String password, String status) {
        try (FileWriter writer = new FileWriter(csvFile, true);
             BufferedWriter bw = new BufferedWriter(writer)) {
            bw.write(employeeId + "," + username + "," + status + "," + password);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
